#include "GameWorld.h"
#include "Key.h"

GameWorld::GameWorld() { buildWorld(); }

std::shared_ptr<Potion> GameWorld::makePotion(int id, const std::string& n, int h) {
    return std::make_shared<Potion>(id, n, h);
}
std::shared_ptr<Weapon> GameWorld::makeWeapon(int id, const std::string& n, int d) {
    return std::make_shared<Weapon>(id, n, d);
}
std::shared_ptr<Key> GameWorld::makeKey(int id, const std::string& n, int targetLoc) {
    return std::make_shared<Key>(id, n, targetLoc);
}
std::shared_ptr<Puzzle> GameWorld::makePuzzle(PuzzleType t, const std::string& desc,
                                               const std::string& ans, int dmg,
                                               std::vector<PuzzleOption> opts) {
    return std::make_shared<Puzzle>(t, desc, ans, dmg, opts);
}

static std::vector<std::shared_ptr<Item>> itemList(
    std::initializer_list<std::shared_ptr<Item>> items) {
    return std::vector<std::shared_ptr<Item>>(items);
}

