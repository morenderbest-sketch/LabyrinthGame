void GameWorld::buildWorld() {

    // ════════════════════════════════════════════
    // ЛОК 1 — Входной зал  (ОТКРЫТА с старта)
    // Сундук 1 (row=2,col=5): открытый, Малое зелье
    // NPC Старый Том (row=4,col=8): даёт зелье, подсказки
    // Дверь → лок 2 (row=3,col=13): ЗАГАДКА про звезду
    // Подсказка: "подними голову ночью"
    // ════════════════════════════════════════════
    {
        std::vector<std::string> map = {
            "##############",
            "#............#",
            "#....C.......#",
            "#............D",
            "#.......N....#",
            "#............#",
            "#..@.........#",
            "##############"
        };
        auto puzzle = makePuzzle(PuzzleType::RIDDLE,
            "Дверь заперта. Надпись на камне:\n"
            "«Я рождаюсь ночью, умираю днём.\n"
            " У меня тысяча братьев, но нет отца.\n"
            " Что я?»\n"
            "Подсказка: подними голову в ясную ночь — ты увидишь их тысячи.\n"
            "Ответ (одно слово):", "звезда", 15);
        auto loc = std::make_shared<Location>(
            1, "Входной зал",
            "Вы очнулись в полутёмном зале. Сырые камни покрыты мхом.\n"
            "Мерцает одинокий факел. На востоке — массивная дверь [D].\n"
            "В углу сидит старик [N]. У стены стоит сундук [C].\n"
            "Управление: w/a/s/d — движение, e — взаимодействие, i — инвентарь, m — сохранить",
            map, LocationState::OPEN, puzzle, 15,
            std::vector<DoorInfo>{{3,13,2}}, false, -1,
            "Подсказка к двери: посмотри на ночное небо.");
        loc->addChest(std::make_shared<Chest>(1,
            itemList({makePotion(1, "Малое зелье лечения", 20)}), nullptr), 2, 5);
        loc->addNPC(makeNPC_OldMan(), 4, 8);
        locations_.push_back(loc);
    }

    // ════════════════════════════════════════════
    // ЛОК 2 — Зал Стражей  (закрыта: загадка-звезда)
    //
    // [ЦЕПОЧКА A]
    //   Сундук 2 (row=4,col=9): закрыт ЗАГАДКОЙ Фибоначчи
    //   → содержит Ключ от Библиотеки (открывает лок 3)
    //   → содержит Ржавый меч
    //
    // NPC Страж (row=2,col=3): враждебный, при победе ничего
    // Дверь запад → лок 1 (свободная)
    // Дверь восток → лок 3: ТРЕБУЕТ КЛЮЧ (keyItemId=201)
    // ════════════════════════════════════════════
    {
        std::vector<std::string> map = {
            "##############",
            "#............#",
            "#..N.........#",
            "D............#",
            "#........C...#",
            "#............D",
            "#.@..........#",
            "##############"
        };
        // Дверь в лок 1 — открытая (уже открыта)
        // Дверь в лок 3 — требует ключ id=201
        auto puzzleDoor = makePuzzle(PuzzleType::RIDDLE,
            "Дверь заперта. Надпись:\n"
            "«Я рождаюсь ночью, умираю днём.\n"
            " Тысяча братьев — нет отца. Что я?»\n"
            "Подсказка: ночное небо полно ими.\n"
            "Ответ:", "звезда", 15);
        // Лок 2 открывается загадкой звезды (та же что на двери лок 1)
        auto loc = std::make_shared<Location>(
            2, "Зал Стражей",
            "Огромный тёмный зал. Ряды ржавых доспехов вдоль стен.\n"
            "Призрак Стражника [N] охраняет зал — победи его и обыщи тело.\n"
            "В глубине зала — сундук [C]. На востоке дверь [D] — нужен ключ.\n"
            "Подсказка: ключ лежит в сундуке. Сундук откроет ряд чисел.",
            map, LocationState::CLOSED, puzzleDoor, 15,
            std::vector<DoorInfo>{{3,0,1},{5,13,3}}, false,
            -1,   // loc2 opens with puzzle (no key needed to enter)
            "Подсказка: реши загадку про ночное небо.");

        // Сундук 2: закрыт загадкой Фибоначчи, содержит ключ+меч
        auto chestPuzz = makePuzzle(PuzzleType::SEQUENCE,
            "На замке выбиты цифры: 1, 1, 2, 3, 5, __\n"
            "Подсказка: каждое число — сумма двух предыдущих.\n"
            "Введите следующее число:", "8", 10);
        loc->addChest(std::make_shared<Chest>(2, itemList({
            makeKey(201, "Ключ от Библиотеки", 3),
            makeWeapon(1, "Ржавый меч", 8)
        }), chestPuzz), 4, 9);
        loc->addNPC(makeNPC_Guard(), 2, 3);
        locations_.push_back(loc);
    }

    // ════════════════════════════════════════════
    // ЛОК 3 — Библиотека Теней  (закрыта: ключ id=201)
    //
    // [ЦЕПОЧКА B]
    //   Сундук 3 (row=2,col=3): открытый → Зелья
    //   Сундук 4 (row=5,col=10): закрыт ЗАГАДКОЙ (слово задом)
    //     → содержит Манускрипт (id=301, это Key с targetLoc=0 — спец. предмет)
    //     → содержит Клинок теней
    //   NPC Мортимер (row=3,col=12): просит Манускрипт
    //     → если у игрока есть Манускрипт (id=301), даёт Ключ от лок 4 (id=202)
    //
    // Дверь восток → лок 4: ТРЕБУЕТ КЛЮЧ id=202
    // ════════════════════════════════════════════
    {
        std::vector<std::string> map = {
            "################",
            "#..............#",
            "#..C...........D",
            "#...........N..#",
            "D..............#",
            "#.........C....#",
            "#..@...........#",
            "################"
        };
        auto loc = std::make_shared<Location>(
            3, "Библиотека Теней",
            "Огромные полки уходят во тьму. Книги рассыпались в прах.\n"
            "Слышен шелест несуществующих страниц. В глубине — NPC [N].\n"
            "Два сундука [C]. Библиотекарь знает про ключ к следующей двери.\n"
            "Подсказка: поговори с Мортимером — он объяснит что делать.",
            map, LocationState::CLOSED, nullptr, 0,
            std::vector<DoorInfo>{{4,0,2},{2,15,4}}, false,
            201,  // to ENTER loc3 you need key 201 (from chest in loc2)
            "Подсказка: ключ от библиотеки лежит в сундуке зала стражей.");

        // Сундук 3: открытый, зелья
        loc->addChest(std::make_shared<Chest>(3, itemList({
            makePotion(2, "Большое зелье лечения", 50),
            makePotion(3, "Малое зелье лечения", 20)
        }), nullptr), 2, 3);

        // Сундук 4: закрыт загадкой — слово АНЕРОМА задом
        // содержит Манускрипт (Key id=301, targetLoc=0 — квест-предмет)
        // и Клинок теней
        auto chestPuzz2 = makePuzzle(PuzzleType::WORD,
            "Замок сундука покрыт надписью: « А Н Е Р О М »\n"
            "Подсказка: прочитай слово справа налево.\n"
            "Ответ:", "морена", 10);
        loc->addChest(std::make_shared<Chest>(4, itemList({
            makeKey(301, "Древний Манускрипт", 0),  // квест-предмет (targetLoc=0)
            makeWeapon(2, "Клинок теней", 15)
        }), chestPuzz2), 5, 10);

        loc->addNPC(makeNPC_Librarian(), 3, 12);
        locations_.push_back(loc);
    }

    // ════════════════════════════════════════════
    // ЛОК 4 — Зал Духов  (закрыта: ключ id=202)
    //
    // [ЦЕПОЧКА C]
    //   Сундук 5 (row=4,col=12): открытый — Эликсир + Призрачный клинок
    //   NPC Дух (row=2,col=5): загадка-диалог "карта"
    //     → при верном ответе открывает лок 5
    //
    // Дверь восток → лок 5: открывается только через диалог с Духом
    // ════════════════════════════════════════════
    {
        std::vector<std::string> map = {
            "####################",
            "#..................#",
            "#....N.............#",
            "D..................#",
            "#...........C......#",
            "#..................D",
            "#..@...............#",
            "####################"
        };
        auto loc = std::make_shared<Location>(
            4, "Зал Духов",
            "Здесь обитают призраки. Воздух ледяной.\n"
            "В центре парит светящийся Дух [N] — он охраняет путь вперёд.\n"
            "Сундук [C] у стены. Восточная дверь [D] закрыта.\n"
            "Подсказка: поговори с Духом — только он может открыть путь.",
            map, LocationState::CLOSED, nullptr, 0,
            std::vector<DoorInfo>{{3,0,3},{5,19,5}}, false, 202,
            "Подсказка: ключ от этого зала хранит библиотекарь Мортимер.");

        loc->addChest(std::make_shared<Chest>(5, itemList({
            makePotion(4, "Эликсир жизни", 75),
            makeWeapon(3, "Призрачный клинок", 20)
        }), nullptr), 4, 12);
        loc->addNPC(makeNPC_Ghost(), 2, 5);
        locations_.push_back(loc);
    }

    // ════════════════════════════════════════════
    // ЛОК 5 — Тронный зал  (закрыта: открывает Дух)
    // Финальная. Выход [E] — победа.
    // ════════════════════════════════════════════
    {
        std::vector<std::string> map = {
            "####################",
            "#.........E........#",
            "#..................#",
            "D..................#",
            "#..................#",
            "#..................#",
            "#..@...............#",
            "####################"
        };
        auto loc = std::make_shared<Location>(
            5, "Тронный зал",
            "Огромный зал с пустым троном. Над ним — проём в небо [E].\n"
            "Дневной свет! Свобода совсем рядом.\n"
            "Подойдите к выходу [E] и взаимодействуйте — вы свободны!",
            map, LocationState::CLOSED, nullptr, 0,
            std::vector<DoorInfo>{{3,0,4},{1,10,-1}}, true);
        locations_.push_back(loc);
    }
}

Location* GameWorld::getLocation(int id) {
    for (auto& loc : locations_) if (loc->getId() == id) return loc.get();
    return nullptr;
}
const std::vector<std::shared_ptr<Location>>& GameWorld::getLocations() const {
    return locations_;
}
Chest* GameWorld::getChest(int id) {
    for (auto& loc : locations_)
        for (auto& c : loc->getChests())
            if (c->getId() == id) return c.get();
    return nullptr;
}
NPC* GameWorld::getNPC(int id) {
    for (auto& loc : locations_)
        for (auto& n : loc->getNPCs())
            if (n->getId() == id) return n.get();
    return nullptr;
}
